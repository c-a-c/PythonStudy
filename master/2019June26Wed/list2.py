int_str = [1,"2",3,"4",5,"6",7,"8",9,]

# Use Range Function
print("Use Range Function")
for i in range(len(int_str)):
	print(int_str[i], type(int_str[i]))
print()

# Get One Element
print("Get One Element")
for i in int_str:
	print(i, type(i))
print()

# Print List
print("Print List")
print(int_str, type(int_str))


